<script>
import{Line} from 'vue-chartjs';

export default {
    extends: Line,
    props:{
        label:{
            type: String
        },
        chartData:{
            type: Array
        },
        options:{
            type: Object
        }
    },
    mounted(){
        var dates = this.chartData.map(d => d.date);
        var totals = this.chartData.map(d => d.total);
    
        this.renderChart({
            labels: dates,
            datasets:[{
                label: this.label,
                data: totals
            }],
            
        },this.options);
    }
}
</script>
